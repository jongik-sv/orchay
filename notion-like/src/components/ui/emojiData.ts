/**
 * Constants for emoji picker behavior
 */
export const RECENT_EMOJI_KEY = 'iconPicker_recentEmojis';
export const MAX_RECENT_EMOJIS = 10;
export const EMOJI_GRID_COLUMNS = 8;

/**
 * Get recent emojis from localStorage
 */
export function getRecentEmojis(): string[] {
  if (typeof window === 'undefined') {
    return [];
  }

  try {
    const stored = localStorage.getItem(RECENT_EMOJI_KEY);
    return stored ? JSON.parse(stored) : [];
  } catch (error) {
    console.error('[IconPicker] Failed to parse recent emojis:', error);
    return [];
  }
}

/**
 * Save emoji to recent emojis list
 */
export function saveRecentEmoji(emoji: string): void {
  if (typeof window === 'undefined') {
    return;
  }

  try {
    let recent = getRecentEmojis();

    // Remove if already exists (to move to front)
    recent = recent.filter((e) => e !== emoji);

    // Add to front
    recent.unshift(emoji);

    // Keep only MAX_RECENT_EMOJIS
    recent = recent.slice(0, MAX_RECENT_EMOJIS);

    localStorage.setItem(RECENT_EMOJI_KEY, JSON.stringify(recent));
  } catch (error) {
    console.error('[IconPicker] Failed to save recent emoji:', error);
  }
}

/**
 * Clear recent emojis
 */
export function clearRecentEmojis(): void {
  if (typeof window === 'undefined') {
    return;
  }

  try {
    localStorage.removeItem(RECENT_EMOJI_KEY);
  } catch (error) {
    console.error('[IconPicker] Failed to clear recent emojis:', error);
  }
}
